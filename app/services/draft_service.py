# ── LLM Draft Generation ─────────────────────────────────────────────────

def _build_user_message(req: DraftGenerateRequest) -> str:
    """Build the user message from the request for the LLM."""
    parts = [f"Document Type: {req.document_type.value}"]

    # Sender
    parts.append(f"\n--- SENDER / APPLICANT ---")
    parts.append(f"Name: {req.sender.full_name}")
    parts.append(f"Address: {req.sender.address}")
    if req.sender.phone:
        parts.append(f"Phone: {req.sender.phone}")
    if req.sender.email:
        parts.append(f"Email: {req.sender.email}")

    # Recipient
    if req.recipient:
        parts.append(f"\n--- RECIPIENT ---")
        parts.append(f"Name: {req.recipient.full_name}")
        parts.append(f"Address: {req.recipient.address}")
        if req.recipient.phone:
            parts.append(f"Phone: {req.recipient.phone}")

    # Law context (from search results)
    if req.act_name or req.law_section_id:
        parts.append(f"\n--- APPLICABLE LAW ---")
        if req.act_name:
            parts.append(f"Act: {req.act_name}")
        if req.law_section_id:
            parts.append(f"Section: {req.law_section_id}")
        if req.section_text:
            parts.append(f"Section Text: {req.section_text[:500]}")

    # Subject / Incident
    parts.append(f"\n--- SUBJECT / INCIDENT ---")
    parts.append(f"Description: {req.subject}")
    if req.incident_date:
        parts.append(f"Date of Incident: {req.incident_date.isoformat()}")
    if req.incident_location:
        parts.append(f"Location: {req.incident_location}")
    if req.desired_outcome:
        parts.append(f"Desired Outcome: {req.desired_outcome}")

    # Rental-specific
    if req.document_type == DocumentType.RENTAL_AGREEMENT and req.rental_details:
        rd = req.rental_details
        parts.append(f"\n--- RENTAL DETAILS ---")
        parts.append(f"Property Address: {rd.property_address}")
        parts.append(f"Monthly Rent: ₹{rd.monthly_rent:,.2f}")
        parts.append(f"Security Deposit: ₹{rd.security_deposit:,.2f}")
        parts.append(f"Start Date: {rd.agreement_start_date.isoformat()}")
        parts.append(f"Duration: {rd.duration_months} months")
        # Clauses
        enabled = []
        if rd.clauses.no_pets_allowed:
            enabled.append("No Pets Allowed: Tenant shall not keep any pets on the premises.")
        if rd.clauses.lock_in_period:
            enabled.append("Lock-in Period: Neither party may terminate the agreement during the first 11 months.")
        if rd.clauses.maintenance_by_tenant:
            enabled.append("Maintenance by Tenant: Tenant is responsible for all minor repairs and routine maintenance.")
        if enabled:
            parts.append(f"\nAdditional Clauses to Include:")
            for c in enabled:
                parts.append(f"  - {c}")

    # Language
    lang_label = "Hindi" if req.language == "hi" else "English"
    parts.append(f"\nDraft Language: {lang_label}")

    return "\n".join(parts)


async def generate_draft_text(req: DraftGenerateRequest) -> str:
    """Generate document text using the LLM fallback pipeline.

    Args:
        req: The validated draft generation request.

    Returns:
        The AI-generated document text as a plain string.

    Raises:
        RuntimeError if all LLM providers fail.
    """
    system_prompt = PROMPTS.get(req.document_type, _LEGAL_NOTICE_PROMPT)
    user_message = _build_user_message(req)

    logger.info(f"Generating {req.document_type.value} draft ({len(user_message)} chars)")

    # The existing fallback function returns parsed JSON, but our prompts
    # ask for plain text.  We call directly through the providers instead.
    from app.services.generate_service import (
        _call_openai, _call_gemini, _call_grok
    )
    from app.config import settings

    for provider in settings.llm_providers:
        try:
            logger.info(f"Attempting draft with {provider.upper()}")

            if provider == "openai" and settings.OPENAI_API_KEY:
                raw = await _call_openai(
                    system_prompt, user_message, response_format="text"
                )
            elif provider == "gemini" and settings.GEMINI_API_KEY:
                raw = await _call_gemini(system_prompt, user_message)
            elif provider == "grok" and settings.GROK_API_KEY:
                raw = await _call_grok(
                    system_prompt, user_message, response_format="text"
                )
            else:
                continue

            if raw and raw.strip():
                logger.info(f"Draft generated successfully via {provider.upper()} ({len(raw)} chars)")
                return raw.strip()

        except Exception as e:
            logger.error(f"{provider.upper()} failed for draft: {e}")
            continue

    # Fallback to mock/template-based generation when no API keys available
    logger.warning("All LLM providers unavailable, using template-based mock generation")
    return _generate_mock_draft(req)


