# ── PDF Engine (ReportLab) ────────────────────────────────────────────────

def _footer_callback(canvas, doc):
    """Draw the footer on every page — small professional line only."""
    canvas.saveState()
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(HexColor("#666666"))
    page_width = A4[0]
    canvas.drawCentredString(
        page_width / 2, 15 * mm,
        FOOTER_TEXT
    )
    # Page number
    canvas.drawRightString(
        page_width - 20 * mm, 15 * mm,
        f"Page {doc.page}"
    )
    canvas.restoreState()


def export_to_pdf(
    draft_text: str,
    document_type: DocumentType,
    title: Optional[str] = None,
) -> io.BytesIO:
    """Render finalized document text as a professionally formatted PDF.

    Returns a BytesIO buffer — never touches the filesystem.

    Args:
        draft_text: The finalized document text.
        document_type: Used for the PDF title if no custom title provided.
        title: Optional custom title.

    Returns:
        io.BytesIO containing the PDF bytes, seeked to position 0.
    """
    buffer = io.BytesIO()

    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        topMargin=25 * mm,
        bottomMargin=25 * mm,
        leftMargin=20 * mm,
        rightMargin=20 * mm,
        title=title or DOCUMENT_TITLES.get(document_type, "Legal Document"),
        author="LexIndia AI",
    )

    styles = getSampleStyleSheet()

    # ── Custom Styles ─────────────────────────────────────────────────
    styles.add(ParagraphStyle(
        "DocTitle",
        parent=styles["Heading1"],
        fontSize=16,
        alignment=TA_CENTER,
        spaceAfter=12,
        textColor=HexColor("#1a1a2e"),
        fontName="Helvetica-Bold",
    ))

    styles.add(ParagraphStyle(
        "Disclaimer",
        parent=styles["Normal"],
        fontSize=9,
        fontName="Helvetica-BoldOblique",
        textColor=HexColor("#cc0000"),
        alignment=TA_JUSTIFY,
        borderWidth=1,
        borderColor=HexColor("#cc0000"),
        borderPadding=8,
        spaceBefore=6,
        spaceAfter=16,
        backColor=HexColor("#fff5f5"),
    ))

    styles.add(ParagraphStyle(
        "DocBody",
        parent=styles["Normal"],
        fontSize=11,
        leading=16,
        alignment=TA_JUSTIFY,
        fontName="Helvetica",
        spaceAfter=6,
    ))

    styles.add(ParagraphStyle(
        "DocDate",
        parent=styles["Normal"],
        fontSize=10,
        alignment=TA_LEFT,
        textColor=HexColor("#555555"),
        spaceAfter=12,
    ))

    # ── Build Story ───────────────────────────────────────────────────
    story = []

    # Date
    story.append(Paragraph(
        f"Date: {datetime.now().strftime('%d %B %Y')}",
        styles["DocDate"]
    ))

    # Title
    doc_title = title or DOCUMENT_TITLES.get(document_type, "LEGAL DOCUMENT")
    story.append(Paragraph(doc_title, styles["DocTitle"]))

    # Horizontal rule
    story.append(HRFlowable(
        width="100%", thickness=1.5, color=HexColor("#1a1a2e"),
        spaceBefore=4, spaceAfter=12
    ))

    # ── Mandatory Disclaimer (Page 1, Bold) ───────────────────────────
    story.append(Paragraph(
        f"<b>⚠ DISCLAIMER:</b> {DISCLAIMER_TEXT}",
        styles["Disclaimer"]
    ))

    story.append(Spacer(1, 8))

    # ── Document Body ─────────────────────────────────────────────────
    # Split the draft text into paragraphs and render each
    paragraphs = draft_text.split("\n")
    for para_text in paragraphs:
        stripped = para_text.strip()
        if not stripped:
            story.append(Spacer(1, 6))
            continue

        # Detect headings (lines in ALL CAPS or starting with numbers like "1.")
        if stripped.isupper() and len(stripped) < 100:
            story.append(Spacer(1, 8))
            story.append(Paragraph(
                f"<b>{stripped}</b>",
                styles["Heading3"]
            ))
        elif stripped.startswith(("---", "___", "===")):
            story.append(HRFlowable(
                width="100%", thickness=0.5, color=HexColor("#cccccc"),
                spaceBefore=4, spaceAfter=4
            ))
        else:
            # Escape XML-sensitive characters for ReportLab
            safe = (
                stripped
                .replace("&", "&amp;")
                .replace("<", "&lt;")
                .replace(">", "&gt;")
            )
            story.append(Paragraph(safe, styles["DocBody"]))

    # Build the PDF
    doc.build(story, onFirstPage=_footer_callback, onLaterPages=_footer_callback)

    buffer.seek(0)
    logger.info(f"PDF generated: {buffer.getbuffer().nbytes} bytes")
    return buffer
